# =============================================================================
# Librairies
# =============================================================================
from trackpy import Sequence, Tracker

# =============================================================================
# Sequence
# =============================================================================
seq = Sequence("img_exp")
seq.load_all()

# =============================================================================
# Create tracker
# =============================================================================
tracker = Tracker(seq)

# =============================================================================
# Add target
# =============================================================================
tracker.add_target()
tracker.add_target()

# =============================================================================
# Run tracking
# =============================================================================
tracker.run()

# =============================================================================
# Display frames
# =============================================================================
tracker.display_tracking(save_dir="res/frames")

# =============================================================================
# Plot center tracking
# =============================================================================
for target in tracker.targets:
    target.display_center_tracking(save_dir="res")

# =============================================================================
# Export target trajectory
# =============================================================================
tracker.export_trajectories(save_dir="res/txt", targets=tracker.targets)


