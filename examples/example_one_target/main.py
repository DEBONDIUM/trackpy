# =============================================================================
# Librairies
# =============================================================================
from trackpy import Sequence, Tracker

# =============================================================================
# Sequence
# =============================================================================
seq = Sequence("img_exp")
seq.load_all()

# =============================================================================
# Create tracker
# =============================================================================
tracker = Tracker(seq, threshold=70)

# =============================================================================
# Add target
# =============================================================================
tracker.add_target()

# =============================================================================
# Run tracking
# =============================================================================
tracker.run()

# =============================================================================
# Display frames
# =============================================================================
tracker.display_tracking(save_dir="res/frames")

# =============================================================================
# Plot center tracking
# =============================================================================
target = tracker.targets[0]
target.display_center_tracking(save_dir="res")

# =============================================================================
# Export target trajectory
# =============================================================================
seq.export_all(save_dir="res/txt", targets=tracker.targets)